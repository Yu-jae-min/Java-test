package ch1;

class Hello {
  public static void main(String[] args){ // main 메서드 선언부
    // 실행될 문장들을 적는다.

    System.out.println("Hello world.");
  }
}

/*
 * [ 참고 ]
 * 1. main 메서드는 Java 애플리케이션의 시작점이므로 main 메서드 없이는 애플리케이션이 실행될 수 없다.
 * 2. 하나의 소스 파일에 둘 이상의 클래스를 정의하는 것도 가능하지만, 소스파일의 이름은 public class의 이름과 일치해야한다. (ex 소스 파일 이름이 Hello.java 이므로 public class는 Hello가 되어야한다.)
 */